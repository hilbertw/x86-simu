static unsigned char port_2eh,dev_num=-1;
static bool  active;

 void lpc_handler(
	  void *lpContext,
	  ADDR_RANGE_CMD op, 
	  unsigned int dwAddress,           // start dwAddress on 32bit bus
	  char *pszBuff,                    // optional 
	  unsigned int nBytes )            // in or out buffer
 {
	 unsigned char a0=dwAddress&1;
	 switch(op)
	 {
	 case ADDR_RANGE_READ:  
		 *pszBuff=(a0==0)?port_2eh:
			 (port_2eh<0x30)? lpc_common[port_2eh]:
			 (dev_num!=-1&& active)?lpc[dev_num][port_2eh-0x30].v:0xff;

		 break;
	 case ADDR_RANGE_WRITE: if(a0==0)
							{
								if((unsigned char)*pszBuff==0x87)
								{
									if( port_2eh==0x87)active=true;
								}
								else if((unsigned char)*pszBuff==0xaa)active=false;

								port_2eh=(unsigned char)*pszBuff;
							}
							else if (active)
							{	

					

								if(port_2eh<0x30) 
								{
									lpc_common[port_2eh]=(unsigned char)*pszBuff;
									if(port_2eh==7)
									{
										dev_num=lpc_common[7];

										if(dev_num>sizeof(lpc)/sizeof(lpc[0]))
												dev_num=-1;
									}
								}
								else if(dev_num<=MAX_DEV_NUM)
								{
									if (dev_num!=-1&& !lpc[dev_num][port_2eh-0x30].readonly)
									lpc[dev_num][port_2eh-0x30].v=(unsigned char)*pszBuff;
								}
							}
		 break;
	 default:
		 return;
	 }
#if  LOG_SIO

	 if(a0)
	 log5(
		 "SIO",
		 op==ADDR_RANGE_READ?'r':'w',
		 port_2eh,	
 		nBytes==1? (unsigned char)pszBuff[0]:
		nBytes==2? *(unsigned short *)pszBuff:
		nBytes==4? *(unsigned long *)pszBuff:0,dev_num);
#endif
 }
